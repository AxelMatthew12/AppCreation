package pertama;

public class User {
    public float id;
    public String password;

    public void setid(float id) {
        this.id = id;
    }

    public void setpas(String password) {
        this.password = password;
    }

    public float getid() {
        return id;
    }

    public String getpas() {
        return password;
    }
}